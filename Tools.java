package pkg1;
public class Tools {
    public void numbers(int n1) {
        for(int i=0;i<=n1;i++)
        {
            System.out.print(i+" ");
        }
        System.out.println();
    }
    public void even(int n){
        for(int i=0;i<=n;i+=2){
            System.out.print(i+" "); 
        }
        System.out.println();
    }
    public void odd(int n){
        for(int i=1;i<=n;i+=2){
            System.out.print(i+" "); 
            
        }
        System.out.println();    
    }
    public void mul3(int n){
        for(int i=0;i<=n;i+=3){
            System.out.print(i+" ");        
        }
        System.out.println();       
    }
    public void mul4(int n){
        for(int i=0;i<=n;i+=4){
            System.out.print(i+" ");        
        }
        System.out.println();       
    }
    public void mul5(int n){
        for(int i=0;i<=n;i+=5){
            System.out.print(i+" ");        
        }
        System.out.println();       
    }
}

        
    
    
    
        

        
    
     

